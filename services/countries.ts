export const getCountryCode = (input: string): string => {
    if (!input || input.length < 2) return 'NL';
    
    const upper = input.toUpperCase();
    
    // 1. Check if it's already a valid code
    const matchCode = COUNTRIES.find(c => c.code === upper);
    if (matchCode) return matchCode.code;

    // 2. Check if it's a known name
    const matchName = COUNTRIES.find(c => c.name.toUpperCase() === upper);
    if (matchName) return matchName.code;

    // 3. Fallback manual mappings for common languages/variations
    if (upper === 'NIEDERLANDE' || upper === 'HOLLAND' || upper === 'NEDERLAND') return 'NL';
    if (upper === 'DEUTSCHLAND' || upper === 'DUITSLAND') return 'DE';
    if (upper === 'BELGIUM' || upper === 'BELGIEN' || upper === 'BELGIË') return 'BE';
    if (upper === 'FRANCE' || upper === 'FRANKREICH' || upper === 'FRANKRIJK') return 'FR';
    if (upper === 'SUOMI') return 'FI';
    if (upper === 'UK' || upper === 'GREAT BRITAIN' || upper === 'ENGLAND') return 'GB';
    
    // 4. Default to input if it looks like a code (2 chars), otherwise NL
    return input.length === 2 ? upper : 'NL';
};
